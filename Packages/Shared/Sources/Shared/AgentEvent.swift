//
//  AgentEvent.swift
//  Shared — AgentDeck
//
//  Agent event model (SPEC §10.2). Every structured event carries §10.4
//  confidence and a versioned payload (`payloadV` per payload type, §9).
//  The iOS UI binds ONLY to these shared models — never to provider JSON.
//  Constitution #2: never fabricate structured events; uncertain parses
//  degrade to `rawOutput` with low confidence, not invented structure.
//

import Foundation

/// A structured agent event on the shared timeline (§10.2, §10.4).
public struct AgentEvent: Sendable, Equatable, Identifiable {
    public let id: EventID
    public let sessionID: SessionID
    public let agent: AgentIdentifier
    /// Per-session monotonic sequence; the basis of `EventCursor` (§9).
    public let sequence: UInt64
    /// Unix ms.
    public let timestamp: Int64
    /// §10.4 adapter confidence.
    public let confidence: EventConfidence
    public let payload: AgentEventPayload

    public init(
        id: EventID = .random(),
        sessionID: SessionID,
        agent: AgentIdentifier,
        sequence: UInt64,
        timestamp: Int64,
        confidence: EventConfidence,
        payload: AgentEventPayload
    ) {
        self.id = id
        self.sessionID = sessionID
        self.agent = agent
        self.sequence = sequence
        self.timestamp = timestamp
        self.confidence = confidence
        self.payload = payload
    }

    /// The resume cursor pointing AT this event (§9, §14.1).
    public var cursor: EventCursor {
        EventCursor(sessionID: sessionID, lastEventSequence: sequence)
    }

    /// §10.4: whether this event may populate approval surfaces.
    public var isApprovalEligible: Bool { confidence.isApprovalEligible }
}

/// Versioned event payloads (§9: every payload type carries `payloadV`).
public enum AgentEventPayload: Sendable, Equatable {
    case stateChanged(SessionStateChange)
    case messageText(MessageText)
    case toolCallStarted(ToolCall)
    case toolCallFinished(ToolCallResult)
    case commandStarted(CommandStart)
    case commandFinished(CommandResult)
    case fileOperation(FileOperation)
    case diffAvailable(DiffSummary)
    case approvalRequested(ApprovalRequest)
    case approvalResolved(ApprovalResolution)
    case waitingForUser(UserQuestion)
    case plan(PlanUpdate)
    case fileSearch(FileSearchResult)
    case build(BuildReport)
    case test(TestReport)
    case warning(AgentWarning)
    case completed(CompletionResult)
    case failed(AgentErrorInfo)
    /// Degraded output for uncertain parses (§10.4) — shown raw, never
    /// dressed up as structure (Constitution #2).
    case rawOutput(RawOutput)
    /// Transport-level notices (event-gap markers, resume pagination) —
    /// generated by the transport itself, never by an agent, and honest
    /// about degradation (Constitution #2).
    case transport(TransportNotice)

    /// Wire discriminator values (stable; renaming a case must not change them).
    public var kind: String {
        switch self {
        case .stateChanged: "stateChanged"
        case .messageText: "messageText"
        case .toolCallStarted: "toolCallStarted"
        case .toolCallFinished: "toolCallFinished"
        case .commandStarted: "commandStarted"
        case .commandFinished: "commandFinished"
        case .fileOperation: "fileOperation"
        case .diffAvailable: "diffAvailable"
        case .approvalRequested: "approvalRequested"
        case .approvalResolved: "approvalResolved"
        case .waitingForUser: "waitingForUser"
        case .plan: "plan"
        case .fileSearch: "fileSearch"
        case .build: "build"
        case .test: "test"
        case .warning: "warning"
        case .completed: "completed"
        case .failed: "failed"
        case .rawOutput: "rawOutput"
        case .transport: "transport"
        }
    }
}

