/// Transport-notice codes. Decoding is UNKNOWN-TOLERANT: a notice code a
/// build does not recognize decodes as `.unknown` instead of throwing, so
/// newer transport markers never break older peers (§9 forward compat).
public enum TransportNoticeCode: String, Sendable, CaseIterable, Codable {
    /// Events were dropped from persistence at the per-session cap; the
    /// timeline has an honestly-declared hole.
    case eventGap
    /// End of a resume page; metadata carries hasMore + the next cursor.
    case resumePage
    /// Placeholder for codes this build does not recognize.
    case unknown
}

/// A transport-generated notice (never agent output): gap markers and
/// resume-pagination markers. `metadata` carries notice-specific fields
/// (e.g. `hasMore`, `droppedCount`) so new fields don't need new payloadV.
public struct TransportNotice: Sendable, Equatable {
    public static let payloadV: Int64 = 1
    public let code: TransportNoticeCode
    public let message: String
    public let metadata: JSONValue

    public init(code: TransportNoticeCode, message: String, metadata: JSONValue = .object([:])) {
        self.code = code
        self.message = message
        self.metadata = metadata
    }
}

extension TransportNoticeCode: JSONValueConvertible {
    public init(jsonValue: JSONValue) throws {
        guard let raw = jsonValue.stringValue else {
            throw JSONValueDecodingError.invalidValue(
                field: String(describing: Self.self), reason: "code is not a string"
            )
        }
        // Unknown-tolerant on purpose (see type doc).
        self = TransportNoticeCode(rawValue: raw) ?? .unknown
    }

    public func toJSONValue() -> JSONValue { .string(rawValue) }
}
